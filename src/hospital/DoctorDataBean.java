package hospital;

public class DoctorDataBean {
	private String dr_id;
	private String dr_passwd;
	private String dr_name;
	private String dr_tel;
	private String dr_major;
	public String getDr_id() {
		return dr_id;
	}
	public void setDr_id(String dr_id) {
		this.dr_id = dr_id;
	}
	public String getDr_passwd() {
		return dr_passwd;
	}
	public void setDr_passwd(String dr_passwd) {
		this.dr_passwd = dr_passwd;
	}
	public String getDr_name() {
		return dr_name;
	}
	public void setDr_name(String dr_name) {
		this.dr_name = dr_name;
	}
	public String getDr_tel() {
		return dr_tel;
	}
	public void setDr_tel(String dr_tel) {
		this.dr_tel = dr_tel;
	}
	public String getDr_major() {
		return dr_major;
	}
	public void setDr_major(String dr_major) {
		this.dr_major = dr_major;
	}
	
}
